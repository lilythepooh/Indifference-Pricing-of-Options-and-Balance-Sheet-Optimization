function [a]=ComputeA(P)
data=ImportData();
Sb0(:,1)=data(:,2)./1000;
Sa0(:,1)=data(:,3)./1000;
ao=(-2/100000)*[P,-P];
E=eye(395,395);
aq=zeros(792);
aq(2:396,2:396)=E(1:395,1:395)./100;
aq(398:792,398:792)=E(1:395,1:395)./100;
ac=[1/100000,Sa0(2:396,1).',-1/100000,Sb0(2:396,1).';aq];
a=[ao;ac];
end